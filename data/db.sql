DROP TABLE IF EXISTS keyuser;
DROP TABLE IF EXISTS keybar;
DROP TABLE IF EXISTS blacklist;
DROP TABLE IF EXISTS bar;
DROP TABLE IF EXISTS keyword;
DROP TABLE IF EXISTS keycategory;
DROP TABLE IF EXISTS "user";


CREATE TABLE "user" (
  id     SERIAL PRIMARY KEY,
  pseudo VARCHAR NOT NULL,
  email  VARCHAR NOT NULL,
  hash   VARCHAR NOT NULL,
  role   VARCHAR NOT NULL
);

CREATE TABLE keycategory (
  id       SERIAL PRIMARY KEY,
  category VARCHAR NOT NULL

);
CREATE table bar (
  id      SERIAL PRIMARY KEY,
  name    VARCHAR,
  address VARCHAR
);


CREATE TABLE keyword (
  id         SERIAL PRIMARY KEY,
  name       VARCHAR,
  idCategory INT REFERENCES keycategory (id)
);


CREATE TABLE keyuser (
  id        SERIAL PRIMARY KEY,
  idKeyWord INT REFERENCES keyword (id),
  idUser    INT REFERENCES "user" (id)
);

CREATE TABLE keybar (
  id          SERIAL PRIMARY KEY,
  idKeyWord   INT REFERENCES keyword (id),
  nbOccurence INT,
  idBar       INT REFERENCES bar (id)
);

CREATE TABLE blacklist (
  id     SERIAL PRIMARY KEY,
  idBar  INT REFERENCES bar (id),
  idUser INT REFERENCES "user" (id)
);

